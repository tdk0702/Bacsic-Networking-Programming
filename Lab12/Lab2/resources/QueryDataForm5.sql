GO
CREATE TABLE INFO(
	Id INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
	Mname NTEXT,
	Origin NTEXT,
	Price INTEGER,
)
GO
CREATE TABLE THEATERS(
	Id INT NOT NULL,
	Idtheater INT,
	CONSTRAINT pk_theaters PRIMARY KEY (Id,Idtheater)
)
GO
CREATE TABLE SEAT(
	Id INT NOT NULL PRIMARY KEY,
	TName NTEXT,
	Normal INT,
	Vip INT,
	Sweet INT
)
GO
